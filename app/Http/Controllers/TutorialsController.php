    {
        // return $request->input('_token');
        // return $request->_token;
        // return $request->all();
        $tutorial = Tutorial::create([
            'user_id' => $request->user_id,
            'title' => $request->title,
            'content' => $request->content 
        ]);
        if ($tutorial) {
            return redirect('tutorials/'.$tutorial->id)->with('message', 'Tutorial was saved');
        }
    }

